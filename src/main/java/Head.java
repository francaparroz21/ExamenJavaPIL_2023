public class Head {
    private String colourHair;
    private String colourSkin;
    private String colourEyes;

    public Head(String colourHair, String colourSkin, String colourEyes) {
        this.colourHair = colourHair;
        this.colourSkin = colourSkin;
        this.colourEyes = colourEyes;
    }

    public String getColourHair() {
        return colourHair;
    }

    public void setColourHairo(String colorPelo) {
        this.colourHair = colorPelo;
    }

    public String getColourSkin() {
        return colourSkin;
    }

    public void setColourSkin(String colourSkin) {
        this.colourSkin = colourSkin;
    }

    public String getColourEyes() {
        return colourEyes;
    }

    public void setColourEyes(String colourEyes) {
        this.colourEyes = colourEyes;
    }
}
